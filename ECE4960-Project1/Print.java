public class Print {

	public static void Int(int x) {
		System.out.println(x);
	}

	public static void Float(Float x) {
		System.out.println(x);
	}

	public static void Double(double x) {
		System.out.println(x);
	}
	

	public static void String(String x) {
		System.out.println("\n" + x);
	}
}
